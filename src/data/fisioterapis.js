/** Konversi slug URL ke nama spesialisasi
 * "cedera-olahraga" → "Cedera Olahraga"
 */
export function slugToSpesialisasi(slug) {
  return slug
    .split('-')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')
}

/** Konversi nama spesialisasi ke slug URL
 * "Cedera Olahraga" → "cedera-olahraga"
 */
export function spesialisasiToSlug(nama) {
  return nama.toLowerCase().replace(/ /g, '-')
}

/** Helper: jadwal sort order (Besok < Lusa) */
export function jadwalOrder(jadwal) {
  if (jadwal.startsWith('Besok')) return 0
  if (jadwal.startsWith('Lusa')) return 1
  return 2
}
